		ldb		#0
speed	set		*-1
		mul
		tfr		d,y			; duree pattern
	  
        ldd     ,u++
        sta     <ch1rat
        stb     <ch2rat
        ldd     ,u++
        sta     <ch3rat
        stb     <ch4rat
        
        ldd     ,u++
        std     <ch1frq
        ldd     ,u++
        std     <ch2frq
        ldd     ,u++
        std     <ch3frq
        ldd     ,u++
        std     <ch4frq

        bra    playptn

exit    puls    d,x,y,u,dp,cc,pc

* joue un pattern Y=durée
playptn pshs    cc,d,x,y,u
        orcc    #$50
        sts     <savsp
        lds     #$E7C0
        ldd     ,s
*        ldd     #$30
        orb     #8
        tfr     d,x     ; x=buzzer 0
        eorb    #8
        tfr     d,u     ; u=buzzer 1

playlp  set     *

* oscillo 1
        ldd     #0      ; 3
        addd    #0      ; 4
ch1frq  set     *-2     ; freq
        std     <*-5    ; 5
        cmpa    #$80    ; 2 (rap. cyclique)
ch1rat  set     *-1
        blo     ch1zer  ; 3
        stu     ,s      ; 5
        bra     ch1end  ; 3
ch1zer  stx     ,s      ; 5
        bra     ch1end  ; 3
ch1end  set     *       ; 25=3+4+5+2+3+5+3

* oscillo 2
        ldd     #0      ; 3
        addd    #0      ; 4
ch2frq  set     *-2     ; freq
        std     <*-5    ; 5
        cmpa    #$80    ; 2 (rap. cyclique)
ch2rat  set     *-1
        blo     ch2zer  ; 3
        stu     ,s      ; 5
        bra     ch2end  ; 3
ch2zer  stx     ,s      ; 5
        bra     ch2end  ; 3
ch2end  set     *       ; 25=3+4+5+2+3+5+3

* oscillo 3
        ldd     #0      ; 3
        addd    #0      ; 4
ch3frq  set     *-2     ; freq
        std     <*-5    ; 5
        cmpa    #$80    ; 2 (rap. cyclique)
ch3rat  set     *-1
        blo     ch3zer  ; 3
        stu     ,s      ; 5
        bra     ch3end  ; 3
ch3zer  stx     ,s      ; 5
        bra     ch3end  ; 3
ch3end  set     *       ; 25=3+4+5+2+3+5+3

* oscillo 4 / noise
        ldd     #0      ; 3
noise   set     *
        lsla            ; 2
        adca    #0      ; 2
*       nop
*       adda    #0
        addd    #0      ; 4
ch4frq  set     *-2     ; freq
        std     <noise-2    ; 5
        cmpa    #$80    ; 2 (rap. cyclique)
ch4rat  set     *-1
        blo     ch4zer  ; 3
        stu     ,s      ; 5
        bra     ch4end  ; 3
ch4zer  stx     ,s      ; 5
        bra     ch4end  ; 3
ch4end  set     *       ; 29=3+2+2+4+5+2+3+5+3

* bouclage
        leay    -1,y    ; 4
        bne     playlp  ; 3=>111
        
        lds     #0
savsp   set     *-2
        puls    cc,d,x,y,u
		jmp		<newptn

        includ  ZIK
        
        end     main
