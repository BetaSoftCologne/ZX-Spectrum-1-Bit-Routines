newptn  jsr     KTST        ; test clavier
        bcs     exit

		ldy		#$128b		; pas de bruit
		ldd		,u++
		beq		rdseq		; fin pattern?
		bitb	#1			
		beq		initpt0
		ldy		#$4889		; bruit
